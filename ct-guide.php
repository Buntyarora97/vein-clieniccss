                                        <h4>CT GUIDED BIOPSY</h4>
                                       
                                         <p style="text-align:justify;">This is a procedure performed by a radiologist to obtain a small tissue sample through a needle. This is done to make a diagnosis and plan future management. CT scan is used to guide the needle into the lesion in the safest possible manner. This is a minimally invasive procedure and is an alternative to an open surgical biopsy. CT guidance generally results in fewer complications, a faster recovery time, and avoidance of general anesthesia.</p>

<h5>Procedure</h5>
  <p style="text-align:justify;">A definite diagnosis of cancer is almost always based on the histological examination of cell or tissue samples. The procedure used to obtain a specimen for this testing is called a biopsy. Biopsies can be performed by surgical removal of the specimen if the suspicious area is located near the surface of the body or during surgery. If, however, the suspected tumor is located deep inside the body and cannot be seen or felt by the physician, he may decide to perform a CT-guided biopsy. The main advantage of a CT-guided biopsy is that it does not require an incision, but the disadvantage is that in some cases, the needle may not be able to remove enough tissue for analysis.</p>

                                       
