                                        <h4>CT GUIDE FINE NEEDLE ASPIRATION CYTOLOGY (FNAC)
</h4>
                                       
                                        <p style="text-align:justify;">
Computed tomography-guided Fine Needle Aspiration Cytology (CT guided FNAC) is a pinhole non-invasive diagnostic procedure done to extract cells from the target site and study them under the microscope for validation and diagnosis. Cells from target sites (eg. breast, skin or any soft tissue) are extracted with the use of a fine needle/syringe, which is inserted through the skin under the guidance of CT so that the margin of error is minimised.</p>

<h5>
 Why is CT Guided - FNAC done?</h5>
 <ul>
<li>To detect and diagnose cancer in deep seated organs which cannot be accessed with invasive surgical procedures</li>
<li>To evaluate the recurrence of a cancer following therapy and monitor the efficacy of the treatment (chemotherapy) for tumors already diagnosed</li>
<li>To differentiate between benign tumor (localized) and malignant tumor (spreading cancer)</li>
<li>To diagnose breast cancer, prostate cancer, skin cancer, colon / rectum cancer, lung cancer, blood cancer, lymph node cancer</li></ul>
                                     

                                     
