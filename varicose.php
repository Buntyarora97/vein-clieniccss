                        <h4>Varicose Veins</h4>
                        <p style="text-align:justify;">Prominently visible, large, purplish veins appearing through the skin are usually known as varicose veins. A milder version of this is known as spider veins. This condition more commonly affects women and is usually benign. The condition may, however, become painful at times. Veins that are affected with varicose are usually a cosmetic concern only, however, and appear to affect the legs due to the weight borne by them, more often. This condition is most often self-diagnosed and does not require laboratory imaging. In some cases, it may signify an underlying circulatory problem.</p>
