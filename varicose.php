<h5>Why is it Important to Treat?</h5>
<p style="text-align:justify;">The goals of treatment are to relieve symptoms, improve appearance, and prevent complications such as serious skin ulcers or sores, deep vein thrombosis, skin color changes, and bleeding. Bleeding from varicose veins is a medical emergency.</p>
<ul>
<li>● Cosmetic Issues</li>
<li>● Leg Pain</li>
<li>● Swelling around ankles</li>
<li>● Pigmentation of skin (darkening of the skin around ankles)</li>
<li>● Eczema (Dermatitis)</li>
<li>● Formation of wounds and ulcers</li>
<li>● Bleeding from rupture of Varicose Veins</li>
<li>● Inflammation of the veins (Thrombophlebitis)</li>
<li>● Formation of a blood clot (Thrombosis)</li>
<li>● Deep Vein Thrombosis (Clots in deep veins)</li>
<li>● Pulmonary Embolism (Clot traveling to the lungs via the heart)</li>
