                                        <p>Ultrasound- and MRI-guided prostate biopsies are performed to collect tissue samples from the prostate gland for examination by a pathologist to determine whether or not the tissue is cancerous.
Biopsies are most commonly performed under ultrasound guidance. During the procedure, a special biopsy needle is inserted into the prostate gland through the wall of the rectum to remove several small samples of tissue for pathologic analysis. This method is known as transrectal ultrasound (TRUS) guided biopsy.</p>
<p>
The prostate may also be accessed through the perineum (the area of skin between the base of the penis and the rectum). This method is known as the transperineal approach and may be used for one of several reasons:</p>

 <ul><li>if cancer is suspected at the front of the prostate gland too far away from the rectum for TRUS.</li>
 <li>if transrectal ultrasound is not feasible due to prior rectal surgery.</li>
 <li>for some mapping biopsies.</li></ul>
<p>Transrectal ultrasound guided (TRUS) biopsy. Doctors use this test to diagnose prostate cancer. They take samples of tissue from the prostate gland to look for cancer cells. You might have an MRI scan before your transrectal ultrasoundguided biopsyA prostate biopsy is a procedure to remove samples of suspicious tissue from the prostate. The prostate is a small, walnut-shaped gland in men that produces fluid that nourishes and transports sperm.
During a prostate biopsy a needle is used to collect a number of tissue samples from your prostate gland. The procedure is performed by a doctor who specializes in the urinary system and men's sex organs (urologist).
Your urologist may recommend a prostate biopsy if results from initial tests, such as a prostate-specific antigen (PSA) blood test or digital rectal exam, suggest you may have prostate cancer. Tissue samples from the prostate biopsy are examined under a microscope for cell abnormalities that are a sign of prostate cancer. If cancer is present, it is evaluated to determine how quickly it's likely to progress and to determine your best treatment options.</p>

Prostate biopsy samples can be collected in different ways. Your prostate biopsy may involve:

 <ul><li>Passing the needle through the wall of the rectum (transrectal biopsy). This is the most common way of performing a prostate biopsy.</li>
 <li>Inserting the needle through the area of skin between the anus and scrotum (transperineal biopsy). A small cut is made in the area of skin (perineum) between the anus and the scrotum. The biopsy needle is inserted through the cut and into the prostate to draw out a sample of tissue. An MRI or CT scan is generally used to guide this procedure.</li></ul>

                                       
