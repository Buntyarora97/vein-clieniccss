                                        <h4>USG Percutaneous Nephrostomy</h4>
                                       
                                      <p style="text-align:justify;">A percutaneous nephrostomy is the placement of a small, flexible rubber tube(catheter) through your skin into your kidney to drain your urine. Percutaneousnephrostolithotomy (or nephrolithotomy) is the passing of a special medical instrument through your skin into your kidney. This is done to remove kidney stones.
Most stones pass out of the body on their own through urine. When they do not, your health care provider may recommend these procedures. During the procedure, you lie on your stomach on a table. You are given a shot of lidocaine. This is the same medicine your dentist uses to numb your mouth. The provider may give you medicines to help you relax and reduce pain.
If you have nephrostomy only:</p>

 <ul><li>The doctor inserts a needle into your skin. Then the nephrostomy catheter is passed through the needle into your kidney.</li>
 <li>You may feel pressure and discomfort when the catheter is inserted.</li>
 <li>A special type of x-ray is used to make sure the catheter is in the right place.</li></ul>
  <p style="text-align:justify;"><b>Procedure:</b> A retrograde pyelogram is done to locate the stone in the kidney. With a small 1 centimeter incision in the loin, the percutaneous nephrolithotomy (PCN) needle is passed into the pelvis of the kidney. The position of the needle is confirmed by fluoroscopy. A guide wire is passed through the needle into the pelvis. The needle is then withdrawn with the guide wire still inside the pelvis. Over the guide wire the dilators are passed and a working sheath is introduced. A nephroscope is then passed inside and small stones taken out. In case the stone is big it may first have to be crushed using ultrasound probes and then the stone fragments removed. The most difficult portion of the procedure is creating the tract between the kidney and the flank skin. Most of the time this is achieved by advancing a needle from the flank skin into the kidney, known as the 'antegrade' technique. A 'retrograde' technique has recently been updated wherein a thin wire is passed from inside the kidney to outside the flank with the aid of a flexible ureteroscope. This technique may reduce radiation exposure for patient and surgeon</p>
  
