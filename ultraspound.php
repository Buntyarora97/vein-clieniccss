                                        <h4>USG Percutaneous TransHepatic Biliary Drainage</h4>
                                       
                                      <p style="text-align:justify;">Percutaneous transhepatic biliary drainage (PTBD) is a common and effective procedure for the palliation of cholestasis in both malignant and benign biliary obstruction, especially after the application of the B-mode ultrasonography guidance . Success rates for PTBD have been reported at 90% or more, and complication rates have been reported at 3% or less .</p>
<p style="text-align:justify;">However, when applied to patients with a special condition (for example, a nondilated bile duct, patient status post left hepatic lobe resection, patient status post liver transplantation), PTBD has the potential for technical difficulties. In addition, for new practitioners of this procedure, understanding the bile duct anatomy (especially the right hepatic bile duct) can be challenging.
A procedure to drain bile to relieve pressure in the bile ducts caused by a blockage. An x-ray of the liver and bile ducts locates the blockage of bile flow. Images made by ultrasound guide placement of a stent (tube), which remains in the liver. Bile drains through the stent into the small intestine or into a collection bag outside the body. This procedure may relieve jaundice before surgery. Also called percutaneous transhepatic cholangiodrainage and PTCD.</p>
  
