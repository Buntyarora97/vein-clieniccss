                                        <h4>Foam Sclerotherapy?</h4>
                                       
                                        <p style="text-align:justify;">
Foam sclerotherapy is a minimally invasive technique for men and women to eliminate unsightly varicose veins and spider veins.
The procedure involves injecting a foam sclerosant in a blood vessel to close it. The blood reroutes itself through healthy veins, restoring more normal blood flow.</p>
                                        
